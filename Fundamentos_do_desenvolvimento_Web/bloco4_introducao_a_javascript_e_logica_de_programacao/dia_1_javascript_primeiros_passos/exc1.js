let numeroA = 15;
let numeroB = 10;
let resultado;

//Adição (a + b)
resultado = numeroA + numeroB;
console.log(resultado);

//Subtração (a - b)
resultado = numeroA - numeroB;
console.log(resultado + '');

//Multiplicação (a * b)
resultado = numeroA * numeroB;
console.log(resultado + '');

//Divisão (a / b)
resultado = numeroA / numeroB;
console.log(resultado + '');

//Módulo (a % b)
resultado = numeroA % numeroB;
console.log(resultado + '');
